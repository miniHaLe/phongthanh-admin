import { writeFile } from 'node:fs/promises'
import { resolve } from 'node:path'

const PROVINCES_URL =
  'https://raw.githubusercontent.com/zindont/vietnam-address-kit/faa205f656149acbd4c6f03ec1407a2e7cd6dff4/src/data/official/current-provinces.json'
const COMMUNES_URL =
  'https://raw.githubusercontent.com/zindont/vietnam-address-kit/faa205f656149acbd4c6f03ec1407a2e7cd6dff4/src/data/official/current-wards.json'
const OFFICIAL_SOURCE_URL =
  'https://vanban.chinhphu.vn/?pageid=27160&docid=214409'
const OFFICIAL_DOCUMENT =
  'Quyết định 19/2025/QĐ-TTg — Danh mục và mã số các đơn vị hành chính Việt Nam'
const OUTPUT = resolve('src/data/vietnam-administrative-snapshot.ts')

// The pinned conversion aid has one typo; NSO's official service returns
// code 24496 as "Xã Ea Kly" with administrative type "Xã".
const OFFICIAL_COMMUNE_CORRECTIONS = new Map([
  [
    '24496',
    {
      name: 'Ea Kly',
      nameWithType: 'Xã Ea Kly',
      type: 'commune',
      normalizedName: 'ea kly',
    },
  ],
])

async function getJson(url) {
  const response = await fetch(url)
  if (!response.ok) {
    throw new Error(`Cannot download ${url}: HTTP ${response.status}`)
  }
  return response.json()
}

const [rawProvinces, rawCommunes] = await Promise.all([
  getJson(PROVINCES_URL),
  getJson(COMMUNES_URL),
])

if (rawProvinces.length !== 34 || rawCommunes.length !== 3321) {
  throw new Error(
    `Decision 19 count mismatch: ${rawProvinces.length} provinces, ${rawCommunes.length} communes`,
  )
}

const provinceByCode = new Map(rawProvinces.map((item) => [item.code, item]))
const provinces = rawProvinces.map((item) => ({
  code: item.code,
  name: item.nameWithType,
  type: item.type,
}))
const communes = rawCommunes.map((sourceItem) => {
  const item = {
    ...sourceItem,
    ...OFFICIAL_COMMUNE_CORRECTIONS.get(sourceItem.code),
  }
  const province = provinceByCode.get(item.provinceCode)
  if (!province) throw new Error(`Unknown province ${item.provinceCode}`)
  return {
    code: item.code,
    name: item.nameWithType,
    type: item.type,
    normalizedName: item.normalizedName,
    provinceCode: item.provinceCode,
    provinceName: province.nameWithType,
  }
})

if (new Set(provinces.map((item) => item.code)).size !== provinces.length) {
  throw new Error('Duplicate province code')
}
if (new Set(communes.map((item) => item.code)).size !== communes.length) {
  throw new Error('Duplicate commune code')
}

const snapshot = {
  version: 'official-2025.07.01',
  effectiveFrom: '2025-07-01',
  sourceDocument: OFFICIAL_DOCUMENT,
  provinces,
  communes,
}

const source = `// Generated by scripts/generate-vietnam-administrative-snapshot.mjs.
// Official authority: ${OFFICIAL_SOURCE_URL}. Extraction dataset is MIT-licensed
// vietnam-address-kit and is validated here against the official 34/3,321 totals.
import type { VietnamAdministrativeSnapshot } from '@/types/vietnam-administrative-types'

export const VIETNAM_ADMINISTRATIVE_SNAPSHOT: VietnamAdministrativeSnapshot = ${JSON.stringify(snapshot, null, 2)}
`

await writeFile(OUTPUT, source, 'utf8')
console.log(
  `Wrote ${OUTPUT}: ${provinces.length} provinces, ${communes.length} communes`,
)
